package com.quyca.scriptwriter.model;

/**
 * The enum Action state.
 */
public enum QuycaCommandState {
    /**
     * To execute action state.
     */
    TO_EXECUTE,
    /**
     * In execution action state.
     */
    IN_EXECUTION,
    /**
     * Done action state.
     */
    DONE,
    ERROR;

}
